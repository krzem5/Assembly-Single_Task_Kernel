#ifndef _SYS2_ERROR_ERROR_H_
#define _SYS2_ERROR_ERROR_H_ 1
#include <sys2/types.h>



#define SYS2_IS_ERROR(error) (((s64)(error))<0)

#define _SYS2_BUILD_ERROR(id) ((u64)(-((s64)(id))))

#define SYS2_ERROR_OK _SYS2_BUILD_ERROR(0x0000)
#define SYS2_ERROR_INVALID_SYSCALL _SYS2_BUILD_ERROR(0x0001)
#define SYS2_ERROR_INVALID_ARGUMENT(index) _SYS2_BUILD_ERROR((0x0002|((index)<<16)))
#define SYS2_ERROR_NOT_FOUND _SYS2_BUILD_ERROR(0x0003)
#define SYS2_ERROR_INVALID_HANDLE _SYS2_BUILD_ERROR(0x0004)
#define SYS2_ERROR_NO_ACL _SYS2_BUILD_ERROR(0x0005)
#define SYS2_ERROR_DENIED _SYS2_BUILD_ERROR(0x0006)
#define SYS2_ERROR_UNSUPPORTED_OPERATION _SYS2_BUILD_ERROR(0x0007)
#define SYS2_ERROR_NO_SPACE _SYS2_BUILD_ERROR(0x0008)
#define SYS2_ERROR_NO_DATA _SYS2_BUILD_ERROR(0x0009)
#define SYS2_ERROR_INVALID_FORMAT _SYS2_BUILD_ERROR(0x000a)
#define SYS2_ERROR_INVALID_ADDRESS _SYS2_BUILD_ERROR(0x000b)
#define SYS2_ERROR_DISABLED_OPERATION _SYS2_BUILD_ERROR(0x000c)
#define SYS2_ERROR_NO_MEMORY _SYS2_BUILD_ERROR(0x000d)
#define SYS2_ERROR_ALREADY_PRESENT _SYS2_BUILD_ERROR(0x000e)

#define SYS2_ERROR_GET_INDEX(error) ((error)>>16)



typedef u64 sys2_error_t;



#endif
